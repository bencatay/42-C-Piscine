#include <unistd.h>

void	yazdir(char c)

{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)

{
	if (nb == -2147483648)
	{
		write (1, "-", 1);
		write (1, "2", 1);
		nb = 147483648;
	}
	if (nb < 0)
	{
		write (1, "-", 1);
		nb = -nb;
	}	
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else
	{
		yazdir (nb + '0');
	}
}
