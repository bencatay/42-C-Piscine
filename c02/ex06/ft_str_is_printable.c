int	ft_str_is_printable(char *str)

{
	unsigned int	a;

	a = 0;
	if (str[a] == '\0')
	{	
		return (1);
	}
	while (str[a] != '\0')
	{
		if (str[a] >= 32 && str[a] <= 127)
			a++;
		else
			return (0);
	}
	return (1);
}
